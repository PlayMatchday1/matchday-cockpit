-- ============================================================
-- schedule_master — recurring weekly match-slot template
-- ============================================================
-- Snapshot of the legacy MatchDay master-schedule HTML, used to
-- power the /cities Master Schedule tab. Each row is a recurring
-- match slot (city, venue, time, date), seeded one-time from the
-- 2026 schedule HTML. Not the same as mdapi_matches (which is the
-- live booking system, populated via API sync) — schedule_master
-- is the human-curated weekly template the ops team plans from.
--
-- match_time is kept as a free-form string (e.g. "7:00 PM - 8:00 PM"
-- or "9:00 PM") because the legacy data has mixed formats and
-- parsing it client-side gives more flexibility than enforcing a
-- single time/range type at the column level. The UI parses the
-- start hour for sort + day-cell rendering.
-- ============================================================

CREATE TABLE IF NOT EXISTS schedule_master (
  id          uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
  city        text          NOT NULL,
  venue       text          NOT NULL,
  detail      text          NOT NULL,
  match_date  date          NOT NULL,
  match_time  text          NOT NULL,
  max_spots   integer       NOT NULL DEFAULT 0,
  created_at  timestamptz   NOT NULL DEFAULT now(),
  updated_at  timestamptz   NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS schedule_master_date_idx  ON schedule_master(match_date);
CREATE INDEX IF NOT EXISTS schedule_master_city_idx  ON schedule_master(city);
CREATE INDEX IF NOT EXISTS schedule_master_venue_idx ON schedule_master(venue);

ALTER TABLE schedule_master ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS schedule_master_admin_read ON schedule_master;
CREATE POLICY schedule_master_admin_read
  ON schedule_master FOR SELECT TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM app_users
      WHERE LOWER(app_users.email) = LOWER(auth.jwt() ->> 'email')
        AND app_users.is_admin = true
    )
  );

